def predict_image_nn(img: Image.Image) -> dict:
    tensor = IMAGE_TRANSFORM(img).unsqueeze(0).to(DEVICE)
    with torch.no_grad():
        logits = IMAGE_MODEL(tensor)
        probs = torch.softmax(logits, dim=1)[0]

    real_p, fake_p = probs[0].item(), probs[1].item()
    is_fake = fake_p > real_p
    conf = fake_p if is_fake else real_p

    return _build_result(is_fake, conf, fake_p, "EfficientNet-B0 neural network")


def predict_video_nn(video_path: str) -> dict:
    """Run SlowFast inference on a video file."""
    import av

    try:
        container = av.open(video_path)
        stream = container.streams.video[0]
        total = stream.frames or 300

        indices = set(torch.linspace(0, total - 1, 64).long().tolist())
        frames = []
        for i, frame in enumerate(container.decode(video=0)):
            if i in indices:
                pil = frame.to_image().convert("RGB")
                frames.append(VIDEO_FRAME_TRANSFORM(pil))
            if len(frames) >= 64:
                break
        container.close()

        while len(frames) < 64:
            frames.append(frames[-1] if frames else torch.zeros(3, 256, 256))

        video_tensor = torch.stack(frames).permute(1, 0, 2, 3)

        fast_idx = torch.linspace(0, 63, 32).long()
        slow_idx = torch.linspace(0, 63, 8).long()
        fast = video_tensor[:, fast_idx, :, :].unsqueeze(0).to(DEVICE)
        slow = video_tensor[:, slow_idx, :, :].unsqueeze(0).to(DEVICE)

        with torch.no_grad():
            logits = VIDEO_MODEL([slow, fast])
            probs = torch.softmax(logits, dim=1)[0]

        real_p, fake_p = probs[0].item(), probs[1].item()
        is_fake = fake_p > real_p
        conf = fake_p if is_fake else real_p

        return _build_result(is_fake, conf, fake_p, "SlowFast R50 video analysis")

    except Exception as e:
        print(f"Video inference error: {e}")
        return _error_result(f"Video analysis failed: {str(e)}")


def predict_video_with_clip(video_path: str) -> dict:
    """Analyze video frames with CLIP + heuristics."""
    import av

    try:
        container = av.open(video_path)
        total = container.streams.video[0].frames or 300
        n_frames = min(12, total)
        indices = set(torch.linspace(0, total - 1, n_frames).long().tolist())

        frame_fakes = []
        for i, frame in enumerate(container.decode(video=0)):
            if i in indices:
                pil = frame.to_image().convert("RGB")
                # Resize for efficiency
                if max(pil.size) > 512:
                    ratio = 512 / max(pil.size)
                    pil = pil.resize(
                        (int(pil.size[0] * ratio), int(pil.size[1] * ratio)),
                        Image.LANCZOS
                    )

                if CLIP_LOADED:
                    _, clip_fake = predict_clip(pil)
                    h = analyze_heuristic(pil)
                    combined = clip_fake * 0.70 + h["fake_score"] * 0.30
                else:
                    h = analyze_heuristic(pil)
                    combined = h["fake_score"]

                frame_fakes.append(combined)
            if len(frame_fakes) >= n_frames:
                break
        container.close()

        if not frame_fakes:
            return _error_result("No frames could be extracted")

        avg_fake = float(np.mean(frame_fakes))
        is_fake = avg_fake > 0.50
        conf = avg_fake if is_fake else (1.0 - avg_fake)
        conf = float(np.clip(conf, 0.50, 0.98))

        detector = "CLIP + Forensics" if CLIP_LOADED else "Forensic heuristics"
        method = f"{detector} frame-by-frame analysis ({len(frame_fakes)} frames sampled)"

        return _build_result(is_fake, conf, avg_fake, method)

    except Exception as e:
        return _error_result(f"Video analysis error: {str(e)}")


