# CLIP-based deepfake detection (PRIMARY)
# ---------------------------------------------------------------------------

REAL_PROMPTS = [
    "a real photograph taken by a camera",
    "an authentic unedited photograph",
    "a genuine photo of a real scene",
    "a natural photograph with real lighting",
    "a candid photo taken in real life",
    "an unaltered real photograph",
]

FAKE_PROMPTS = [
    "an AI generated image",
    "a deepfake image created by artificial intelligence",
    "a synthetic image generated by a neural network",
    "a digitally manipulated fake image",
    "an image created by a GAN or diffusion model",
    "a computer generated fake photograph",
]


def predict_clip(img: Image.Image) -> dict:
    """Zero-shot deepfake detection using CLIP."""
    all_prompts = REAL_PROMPTS + FAKE_PROMPTS

    inputs = CLIP_PROCESSOR(
        text=all_prompts,
        images=img,
        return_tensors="pt",
        padding=True,
    )
    inputs = {k: v.to(DEVICE) for k, v in inputs.items()}

    with torch.no_grad():
        outputs = CLIP_MODEL(**inputs)
        logits = outputs.logits_per_image[0]  # shape: (num_prompts,)
        probs = torch.softmax(logits, dim=0)

    n_real = len(REAL_PROMPTS)
    real_score = probs[:n_real].sum().item()
    fake_score = probs[n_real:].sum().item()

    # Normalize
    total = real_score + fake_score
    real_score /= total
    fake_score /= total

    return real_score, fake_score


# ---------------------------------------------------------------------------
# Enhanced Heuristic Analysis
# ---------------------------------------------------------------------------

def analyze_heuristic(img: Image.Image) -> dict:
    """
    Enhanced forensic heuristic analysis.
    Returns individual scores and a combined fake_score (0-1).
    """
    gray = np.array(img.convert("L"), dtype=np.float64)
    rgb = np.array(img.convert("RGB"), dtype=np.float64)
    scores = {}

    # 1. Error Level Analysis (ELA)
    try:
        buf = io.BytesIO()
        img.save(buf, "JPEG", quality=85)
        buf.seek(0)
        resaved = np.array(Image.open(buf).convert("RGB"), dtype=np.float64)
        diff = np.abs(rgb - resaved)

        # Analyze block-level variance of ELA
        block_stds = []
        for y in range(0, diff.shape[0] - 16, 16):
            for x in range(0, diff.shape[1] - 16, 16):
                block = diff[y:y+16, x:x+16]
                block_stds.append(np.std(block))

        if block_stds:
            # High variance in ELA blocks suggests manipulation
            ela_std = np.std(block_stds)
            ela_mean = np.mean(block_stds)
            # Manipulated images tend to have inconsistent ELA
            scores["ela"] = float(np.clip(ela_std / (ela_mean + 1e-10) / 2.0, 0, 1))
        else:
            scores["ela"] = 0.5
    except Exception:
        scores["ela"] = 0.5

    # 2. Noise Analysis
    try:
        smoothed = np.array(
            img.convert("L").filter(ImageFilter.MedianFilter(5)),
            dtype=np.float64
        )
        noise = gray - smoothed

        # Analyze noise variance per patch
        patch_vars = []
        for y in range(0, noise.shape[0] - 32, 32):
            for x in range(0, noise.shape[1] - 32, 32):
                patch_vars.append(np.var(noise[y:y+32, x:x+32]))

        if patch_vars:
            # AI-generated: very uniform noise; Real photos: varied noise
            cv = np.std(patch_vars) / (np.mean(patch_vars) + 1e-10)
            # Low cv = uniform noise = likely AI; High cv = natural
            scores["noise"] = float(np.clip(1.0 - cv / 3.0, 0, 1))
        else:
            scores["noise"] = 0.5
    except Exception:
        scores["noise"] = 0.5

    # 3. Frequency Domain Analysis (DCT)
    try:
        from scipy.fft import dct

        # 2D DCT on grayscale
        dct_vals = dct(dct(gray, axis=0, norm='ortho'), axis=1, norm='ortho')
        magnitude = np.abs(dct_vals)

        # AI images tend to have less high-frequency content
        h, w = magnitude.shape
        low_freq = magnitude[:h//4, :w//4].mean()
        high_freq = magnitude[h//4:, w//4:].mean()

        ratio = high_freq / (low_freq + 1e-10)
        # Very low ratio = smooth/AI-like; natural images have more high freq
        scores["frequency"] = float(np.clip(1.0 - ratio * 10, 0, 1))
    except Exception:
        scores["frequency"] = 0.5

    # 4. Color Channel Correlation
    try:
        r = rgb[:, :, 0].flatten()[:10000]
        g = rgb[:, :, 1].flatten()[:10000]
        b = rgb[:, :, 2].flatten()[:10000]

        rg_corr = abs(np.corrcoef(r, g)[0, 1])
        rb_corr = abs(np.corrcoef(r, b)[0, 1])
        gb_corr = abs(np.corrcoef(g, b)[0, 1])

        avg_corr = (rg_corr + rb_corr + gb_corr) / 3.0
        # AI images often have unnaturally high color correlation
        scores["color"] = float(np.clip((avg_corr - 0.5) * 2, 0, 1))
    except Exception:
        scores["color"] = 0.5

    # 5. Texture Smoothness (Laplacian variance)
    try:
        # Laplacian kernel
        lap = np.zeros((gray.shape[0] - 2, gray.shape[1] - 2))
        k = np.array([[0, 1, 0], [1, -4, 1], [0, 1, 0]], dtype=np.float64)
        for i in range(3):
            for j in range(3):
                lap += gray[i:gray.shape[0]-2+i, j:gray.shape[1]-2+j] * k[i, j]

        lap_var = np.var(lap)
        # Low Laplacian variance = smoother = more likely AI
        scores["texture"] = float(np.clip(1.0 - lap_var / 3000.0, 0, 1))
    except Exception:
        scores["texture"] = 0.5

    # Weighted combination
    weights = {"ela": 0.25, "noise": 0.25, "frequency": 0.20, "color": 0.10, "texture": 0.20}
    fake_score = sum(scores[k] * weights[k] for k in weights)
    fake_score = float(np.clip(fake_score, 0.05, 0.95))

    return {
        "fake_score": fake_score,
        "details": scores,
    }


