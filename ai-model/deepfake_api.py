def predict_combined(img: Image.Image) -> dict:
    """
    Primary detection: CLIP (70%) + Heuristic (30%)
    Falls back to heuristic-only if CLIP unavailable.
    """
    heuristic = analyze_heuristic(img)
    h_fake = heuristic["fake_score"]
    h_details = heuristic["details"]
    detail_str = ", ".join(f"{k}={v:.3f}" for k, v in h_details.items())

    if CLIP_LOADED:
        clip_real, clip_fake = predict_clip(img)

        # Weighted combination
        combined_fake = clip_fake * 0.70 + h_fake * 0.30
        combined_fake = float(np.clip(combined_fake, 0.02, 0.98))

        is_fake = combined_fake > 0.50
        conf = combined_fake if is_fake else (1.0 - combined_fake)
        conf = float(np.clip(conf, 0.50, 0.98))

        method = (
            f"CLIP zero-shot ({clip_fake*100:.1f}% fake) + "
            f"Forensic heuristics ({h_fake*100:.1f}% fake). "
            f"Scores: {detail_str}"
        )

        return _build_result(is_fake, conf, combined_fake, method)
    else:
        # Heuristic only
        is_fake = h_fake > 0.50
        conf = h_fake if is_fake else (1.0 - h_fake)
        conf = float(np.clip(conf, 0.50, 0.95))

        method = f"Forensic heuristic analysis (scores: {detail_str})"
        return _build_result(is_fake, conf, h_fake, method)


