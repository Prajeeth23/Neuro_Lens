def _build_result(is_fake, confidence, fake_score, method):
    if is_fake:
        explanation = (
            f"Analysis indicates AI-generated content with {confidence*100:.1f}% confidence. "
            f"Method: {method}."
        )
        regions = [{"x": 10, "y": 15, "w": 35, "h": 40, "label": "Detection zone", "severity": round(fake_score, 2)}]
        if fake_score > 0.8:
            regions.append({"x": 45, "y": 10, "w": 25, "h": 30, "label": "High-confidence area", "severity": round(fake_score * .9, 2)})
    else:
        explanation = (
            f"Analysis indicates authentic media with {confidence*100:.1f}% confidence. "
            f"Method: {method}."
        )
        regions = []

    return {
        "prediction": "FAKE" if is_fake else "REAL",
        "confidence": round(confidence, 4),
        "ai_probability": round(fake_score * 100, 1),
        "explanation": explanation,
        "regions": regions,
    }


def _error_result(msg):
    return {
        "prediction": "REAL",
        "confidence": 0.5,
        "ai_probability": 50.0,
        "explanation": msg,
        "regions": [],
    }


