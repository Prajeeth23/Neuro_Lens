@app.post("/predict")
async def predict(file: UploadFile = File(...)):
    contents = await file.read()
    filename = file.filename or ""
    ext = os.path.splitext(filename)[1].lower()

    is_video = ext in {".mp4", ".avi", ".mov", ".mkv"}

    if is_video:
        # Save temp file for video processing
        tmp_dir = os.environ.get("TEMP", os.environ.get("TMP", "/tmp"))
        tmp_path = os.path.join(tmp_dir, f"deepguard_temp{ext}")
        with open(tmp_path, "wb") as f:
            f.write(contents)

        if VIDEO_MODEL_LOADED:
            result = predict_video_nn(tmp_path)
        else:
            # Use CLIP + heuristics on video frames
            result = predict_video_with_clip(tmp_path)

        try:
            os.remove(tmp_path)
        except Exception:
            pass
        return result

    else:
        # Image
        try:
            img = Image.open(io.BytesIO(contents)).convert("RGB")
            if max(img.size) > 1024:
                ratio = 1024 / max(img.size)
                img = img.resize(
                    (int(img.size[0] * ratio), int(img.size[1] * ratio)),
                    Image.LANCZOS
                )
        except Exception:
            return _error_result("Could not decode image")

        # PRIMARY: CLIP + Heuristic combined detection
        return predict_combined(img)


